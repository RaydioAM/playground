# Dictionaries
# Dictionaries are like lists but can index with any data type
# Indexes for dictionaries are called keys
# Key and associated value is a "key-value pair"

# Assign a dictionary to the myCat value
# Keys are size, color, disposition
    # Keys can be numbers too
# Values are fat, gray, loud
myCat = {'size': 'fat', 'color': 'gray', 'disposition': 'loud'}
print(myCat['size'])
print('My cat has ' + myCat['color'] + ' fur.')
