# Adding parameters to the function

# name is the parameter
def hello(name):
    print('Hello ' + name)

# names below are the arguments
# Argument gets assigned to the name parameter
hello('Alice')
hello('Bob')

# Argument = the value passed in the function call
# Parameter = the variable inside the function
# Return value = output from the function 
# Every function has a return value

print('Hello has ' + str(len('hello')) + ' letters in it.')